void	ft_scrambler(int ***a,int *b,int *******c,int ****d)
{
	int temp;

	temp = ******c;
	******c = **a;
	**a = temp;
	temp =  ***d;
	***d = ******c;
	******c = temp;
	temp =  b;
	b = ***d;
	***d = temp;
	temp = **a;
	**a = b;
	b = temp;
}
